package ControlFlowStatements.ForLoops;

public class ForLoopExample1 {
    public static void main(String[] args) {
        for(int i=0;i<10;i++)
        {
            for(int j=1;j<8;j++)
            {
                System.out.println(" *" );
            }
            System.out.print("*");
        }
    }
}
